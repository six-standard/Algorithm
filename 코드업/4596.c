//문제 : 9×9 격자판에 쓰여진 81개의 자연수가 주어질 때, 이들 중 최대값을 찾고 그 최대값이 몇 행 몇 열에 위치한 수인지 구하는 프로그램을 작성하시오.
//입력 : 첫 째 줄부터 아홉 번째 줄까지 한 줄에 아홉 개씩 자연수가 주어진다. 주어지는 자연수는 100보다 작다.
//출력 : 첫째 줄에 최대값을 출력하고, 둘째 줄에 최대값이 위치한 행 번호와 열 번호를 빈칸을 사이에 두고 차례로 출력한다.

#include <stdio.h>

int main() {
    int val, max=0, pos[2];
    for(int i = 0; i<9; i++) {
        for(int j = 0; j<9; j++) {
            scanf("%d", &val);
            if(max<val)  {
                max=val;
                pos[0] = i;
                pos[1]= j;
            }
        }
    }
    printf("%d\n", max);
    printf("%d %d", pos[0]+1, pos[1]+1);
}